package com.kodnest.projecttunehub.repository;

import com.kodnest.projecttunehub.entity.Playlist;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

/**
 * Repository interface for managing Playlists in the database.
 * This interface extends JpaRepository which provides JPA related methods for use with the Playlist entity.
 */
@Repository
public interface PlaylistRepository extends JpaRepository<Playlist, Integer> {
    Playlist findByName(String name);

}